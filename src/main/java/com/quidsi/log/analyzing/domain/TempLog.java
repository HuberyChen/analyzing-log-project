package com.quidsi.log.analyzing.domain;

/**
 * @author hubery.chen
 */
public class TempLog {

    private int timeId;

    private int id;

    private int logId;

    public int getTimeId() {
        return timeId;
    }

    public void setTimeId(int timeId) {
        this.timeId = timeId;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getLogId() {
        return logId;
    }

    public void setLogId(int logId) {
        this.logId = logId;
    }
}
